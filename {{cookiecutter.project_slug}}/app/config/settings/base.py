def environment_callback(request):
    """
    Callback has to return a list of two values represeting text value and the color
    type of the label displayed in top right corner.
    """
    environment_map = {
        "development": ["Development", "info"],  # info, danger, warning, success
        "staging": ["Staging", "warning"],
        "production": ["Production", "danger"],
    }
    settings_module = os.environ.get("DJANGO_SETTINGS_MODULE", "config.settings.development").split('.')[-1]
    return environment_map.get(settings_module, ["Unknown", "info"])
